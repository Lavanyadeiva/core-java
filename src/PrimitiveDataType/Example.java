package PrimitiveDataType;

public class Example {

	public static void main(String[] args) {
		// boolean example	
boolean IoT=true;
System.out.println(IoT);

//byte example
byte range;
range=117;
System.out.println(range);

//short example
short range1;
range1=-20000;
System.out.println(range1);

//int example
int range2;
range2=157000;
System.out.println(range2);

//long example
long number=7853807334l;
System.out.println(number);

//float example
float number1=77.7124f;
System.out.println(number1);

//double example
double number2=35785.87d;
System.out.println(number2);

//character example
char name='L';
System.out.println(name);

//character ASCII example
char name1=100;//ASCII value of d is 100
System.out.println(name1);

//character example UNICODE value of A is '\u0041'
char name2='\u0041';
System.out.println(name2);

//character UNICODE example value of a is '\u0061'
char name3='\u0061';
System.out.println(name3);

//character UNICODE example value of $ is '\u0024'
char name4='\u0024';
System.out.println(name4);

	}
}
